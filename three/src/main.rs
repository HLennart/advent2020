struct Position {
    x: usize,
    y: usize,
}

fn main() {
    let mut position = Position { x: 0, y: 0 };
    let bottom_row = input.lines().count();
    let mut trees_encountered = 0;
    println!("length: {}", input.lines().nth(0).unwrap().len());
    while position.y < bottom_row - 1 {
        if move_and_wrap(&mut position) == true {
            trees_encountered += 1;
        }
    }
    println!("Trees encountered: {}", trees_encountered);
}

type IsTree = bool;

fn move_and_wrap(pos: &mut Position) -> IsTree {
    pos.x += 3;
    pos.y += 1;
    let row = input.lines().nth(pos.y).unwrap();
    // wrap
    if pos.x > row.len() - 1 {
        pos.x = pos.x - row.len();
    }
    if row.chars().nth(pos.x).unwrap() == '#' {
        true
    } else {
        false
    }
}

